<?php
    class Config {
        var $name, $value;

        function getName() {
            $this->name;
        }
        function setName($new_name) {
            $this->name = $new_name;
        }
        function getValue() {
            $this->value;
        }
        function setValue($new_value) {
            $this->value = $new_value;
        }
    }
?>
